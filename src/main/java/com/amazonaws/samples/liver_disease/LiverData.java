package com.amazonaws.samples.liver_disease;

public class LiverData {
    private int age;
    private int gender;
    private double totalBilirubin;
    private double directBilirubin;
    private int alkalinePhosphotase;
    private int alamineAminotransferase;
    private int aspartateAminotransferase;
    private int totalProteins;
    private int albumin;
    private double albuminGlobulinRatio;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public double getTotalBilirubin() {
        return totalBilirubin;
    }

    public void setTotalBilirubin(double totalBilirubin) {
        this.totalBilirubin = totalBilirubin;
    }

    public double getDirectBilirubin() {
        return directBilirubin;
    }

    public void setDirectBilirubin(double directBilirubin) {
        this.directBilirubin = directBilirubin;
    }

    public int getAlkalinePhosphotase() {
        return alkalinePhosphotase;
    }

    public void setAlkalinePhosphotase(int alkalinePhosphotase) {
        this.alkalinePhosphotase = alkalinePhosphotase;
    }

    public int getAlamineAminotransferase() {
        return alamineAminotransferase;
    }

    public void setAlamineAminotransferase(int alamineAminotransferase) {
        this.alamineAminotransferase = alamineAminotransferase;
    }

    public int getAspartateAminotransferase() {
        return aspartateAminotransferase;
    }

    public void setAspartateAminotransferase(int aspartateAminotransferase) {
        this.aspartateAminotransferase = aspartateAminotransferase;
    }

    public int getTotalProteins() {
        return totalProteins;
    }

    public void setTotalProteins(int totalProteins) {
        this.totalProteins = totalProteins;
    }

    public int getAlbumin() {
        return albumin;
    }

    public void setAlbumin(int albumin) {
        this.albumin = albumin;
    }

    public double getAlbuminGlobulinRatio() {
        return albuminGlobulinRatio;
    }

    public void setAlbuminGlobulinRatio(double albuminGlobulinRatio) {
        this.albuminGlobulinRatio = albuminGlobulinRatio;
    }
}
